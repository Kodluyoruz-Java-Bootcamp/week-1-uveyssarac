public class Product {
    //Ürünün kategorisi, fiyatı, ismi ve stok bilgisi vardır.
    String name;
    String category;
    int price;
    int stock;

    public Product(String name, String category, int price, int stock) {
        this.name = name;
        this.category = category;
        this.price = price;
        this.stock = stock;
    }
}
